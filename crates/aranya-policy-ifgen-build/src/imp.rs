use std::collections::{HashMap, HashSet};

use aranya_policy_ast::{FieldDefinition, Persistence, VType};
use aranya_policy_compiler::compile::target::CompileTarget;
use proc_macro2::{Span, TokenStream};
use quote::quote;

/// Generate rust source code from a policy [`CompileTarget`].
#[allow(clippy::panic)]
pub fn generate_code(target: &CompileTarget) -> String {
    let reachable = collect_reachable_types(target);

    let structs = target
        .struct_defs
        .iter()
        .filter(|(id, _fields)| reachable.contains(id.as_str()))
        .map(|(id, fields)| {
            let doc = format!(" {} policy struct.", id);
            let name = mk_ident(id);
            let names = fields.iter().map(|f| mk_ident(&f.identifier));
            let types = fields.iter().map(|f| vtype_to_rtype(&f.field_type));
            quote! {
                #[doc = #doc]
                #[value]
                pub struct #name {
                    #(pub #names: #types),*
                }
            }
        });

    let enums = target
        .enum_defs
        .iter()
        .filter(|(id, _values)| reachable.contains(id.as_str()))
        .map(|(id, values)| {
            let doc = format!(" {} policy enum.", id);
            let name = mk_ident(id);
            let names = values.iter().map(|(id, _)| mk_ident(id));
            quote! {
                #[doc = #doc]
                #[value]
                pub enum #name {
                    #(#names),*
                }
            }
        });

    let effects = target.effects.iter().map(|s| {
        let fields = target
            .struct_defs
            .get(s)
            .unwrap_or_else(|| panic!("Effect not defined: {s}"));
        let doc = format!(" {} policy effect.", s);
        let ident = mk_ident(s);
        let field_idents = fields.iter().map(|f| mk_ident(&f.identifier));
        let field_types = fields.iter().map(|f| vtype_to_rtype(&f.field_type));
        quote! {
            #[doc = #doc]
            #[effect]
            pub struct #ident {
                #(pub #field_idents: #field_types),*
            }
        }
    });

    let effect_enum = {
        let idents = target.effects.iter().map(mk_ident);
        quote! {
            #[effects]
            pub enum Effect {
                #(
                    #idents(#idents)
                ),*
            }
        }
    };

    let persistent = syn::Ident::new("Persistent", Span::call_site());
    let ephemeral = syn::Ident::new("Ephemeral", Span::call_site());

    let actions = {
        let mut persistent_actions = Vec::new();
        let mut ephemeral_actions = Vec::new();
        let structs = target
            .action_defs
            .iter()
            .map(|(id, def)| {
                match def.persistence {
                    Persistence::Persistent => persistent_actions.push(mk_ident(id)),
                    Persistence::Ephemeral => ephemeral_actions.push(mk_ident(id)),
                }
                let interface = match def.persistence {
                    Persistence::Persistent => &persistent,
                    Persistence::Ephemeral => &ephemeral,
                };
                let doc = format!(" {id} policy action.");
                let ident = mk_ident(id);
                let argnames = def.params.iter().map(|arg| mk_ident(&arg.identifier));
                let argtypes = def.params.iter().map(|arg| vtype_to_rtype(&arg.field_type));
                quote! {
                    #[doc = #doc]
                    #[action(interface = #interface)]
                    pub struct #ident {
                        #(pub #argnames: #argtypes),*
                    }
                }
            })
            .collect::<Vec<_>>();
        quote! {
            #[actions(interface = #persistent)]
            pub enum PersistentAction {
                #( #persistent_actions(#persistent_actions) ),*
            }
            #[actions(interface = #ephemeral)]
            pub enum EphemeralAction {
                #( #ephemeral_actions(#ephemeral_actions) ),*
            }
            #( #structs )*
        }
    };

    prettyplease::unparse(&syn::parse_quote! {
        //! This code is @generated by `policy-ifgen`. DO NOT EDIT.
        #![allow(clippy::duplicated_attributes)]
        #![allow(clippy::enum_variant_names)]
        #![allow(missing_docs)]
        #![allow(non_camel_case_types)]
        #![allow(non_snake_case)]
        #![allow(unused_imports)]

        extern crate alloc;

        use alloc::vec::Vec;

        use aranya_policy_ifgen::{
            macros::{action, actions, effect, effects, value},
            ClientError, Id, Value, Text,
        };

        pub struct #persistent;
        pub struct #ephemeral;

        #(#structs)*
        #(#enums)*

        /// Enum of policy effects that can occur in response to a policy action.
        #effect_enum
        #(#effects)*

        #actions
    })
}

fn vtype_to_rtype(ty: &VType) -> TokenStream {
    match ty {
        VType::String => quote! { Text },
        VType::Bytes => quote! { Vec<u8> },
        VType::Int => quote! { i64 },
        VType::Bool => quote! { bool },
        VType::Id => quote! { Id },
        VType::Struct(st) => {
            let ident = mk_ident(st);
            quote! { #ident }
        }
        VType::Enum(st) => {
            let ident = mk_ident(st);
            quote! { #ident }
        }
        VType::Optional(opt) => {
            let inner = vtype_to_rtype(opt);
            quote! {
                Option<#inner>
            }
        }
    }
}

/// Returns the name of all custom types reachable from actions or effects.
#[allow(clippy::panic)]
fn collect_reachable_types(target: &CompileTarget) -> HashSet<&str> {
    fn visit<'a>(
        struct_defs: &HashMap<&str, &'a [FieldDefinition]>,
        found: &mut HashSet<&'a str>,
        ty: &'a VType,
    ) {
        match ty {
            VType::Struct(s) => {
                if found.insert(s.as_str()) {
                    for field in struct_defs[s.as_str()] {
                        visit(struct_defs, found, &field.field_type);
                    }
                }
            }
            VType::Enum(s) => {
                found.insert(s.as_str());
            }
            VType::Optional(inner) => visit(struct_defs, found, inner),
            _ => {}
        }
    }

    let struct_defs = target
        .struct_defs
        .iter()
        .map(|(id, fields)| (id.as_str(), fields.as_slice()))
        .collect::<HashMap<_, _>>();

    let mut found = HashSet::new();

    for def in target.action_defs.values() {
        for param in &def.params {
            visit(&struct_defs, &mut found, &param.field_type);
        }
    }

    for id in &target.effects {
        let fields = target
            .struct_defs
            .get(id)
            .unwrap_or_else(|| panic!("Effect not defined: {id}"));
        for field in fields {
            visit(&struct_defs, &mut found, &field.field_type);
        }
    }

    found
}

/// Makes an identifier from a string, using raw identifiers (`r#mod`) when necessary.
fn mk_ident(string: impl AsRef<str>) -> syn::Ident {
    let string = string.as_ref();
    syn::parse_str::<syn::Ident>(string)
        .unwrap_or_else(|_| syn::Ident::new_raw(string, Span::call_site()))
}

